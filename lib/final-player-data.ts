// Generate initial sample data for these players
export const generateFinalPlayerData = (): Array<{
  id: number;
  name: string;
  total_volume: number;
  player_volume: number;
  merch_volume: number;
  trend_percent: number;
  opportunity_score: number;
  market_count: number;
  market: string;
  age: number;
  position: string;
  current_team: string;
  nationality: string;
}> => {
  const playerNames = Object.keys(FINAL_PLAYER_METADATA);
  const markets = ["United Kingdom", "United States", "Germany", "Spain", "France", "Italy", "Brazil"];
  
  return playerNames.map((name, index) => {
    const metadata = FINAL_PLAYER_METADATA[name as keyof typeof FINAL_PLAYER_METADATA];
    const baseVolume = Math.floor(Math.random() * 500000) + 50000; // 50K-550K base
    const playerVolume = Math.floor(baseVolume * (0.6 + Math.random() * 0.3)); // 60-90% of total
    const merchVolume = baseVolume - playerVolume;
    const trend = (Math.random() - 0.5) * 80; // ±40% trend
    const marketCount = Math.floor(Math.random() * 5) + 3; // 3-7 markets
    
    // Calculate opportunity score
    const normalizedVolume = Math.min(baseVolume / 10000, 100);
    const normalizedTrend = Math.max(-50, Math.min(50, trend));
    const opportunityScore = Math.round(
      (normalizedVolume * 0.5) + ((normalizedTrend + 50) * 0.3) + (marketCount * 4 * 0.2)
    );
    
    return {
      id: index + 1,
      name,
      total_volume: baseVolume,
      player_volume: playerVolume,
      merch_volume: merchVolume,
      trend_percent: Number(trend.toFixed(1)),
      opportunity_score: opportunityScore,
      market_count: marketCount,
      market: markets[Math.floor(Math.random() * markets.length)],
      age: metadata.age,
      position: metadata.position,
      current_team: metadata.current_team,
      nationality: metadata.nationality
    };
  });
};

